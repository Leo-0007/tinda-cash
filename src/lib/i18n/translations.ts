const translations: Record<Locale, Record<string, string>> = {
  fr: fr as unknown as Record<string, string>,
  en,
  pt,
  es,
  nl,
  de,
  it,
  ln,
};

/**
 * Get a translation for a given key and locale.
 * Falls back to French if the key is not found in the target locale.
 */
export function getTranslation(locale: Locale, key: string): string {
  return translations[locale]?.[key] ?? translations.fr[key] ?? key;
}

/**
 * Create a translator function bound to a locale.
 */
export function createTranslator(locale: Locale) {
  return (key: string, replacements?: Record<string, string>): string => {
    let text = getTranslation(locale, key);
    if (replacements) {
      Object.entries(replacements).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, v);
      });
    }
    return text;
  };
}
